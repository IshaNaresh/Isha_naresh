let currentQuestionIndex = 0;
let score = 0;
let startTime;
let timerInterval;

function startTimer() {
    startTime = new Date();
    timerInterval = setInterval(updateTimer, 1000);
}

function updateTimer() {
    const now = new Date();
    const elapsedTime = Math.floor((now - startTime) / 1000);
    const minutes = Math.floor(elapsedTime / 60);
    const seconds = elapsedTime % 60;
    const timerElement = document.getElementById('timer');
    timerElement.innerText = `Timer: ${minutes}:${seconds < 10 ? '0' + seconds : seconds}`;
}

function loadQuestion() {
    if (currentQuestionIndex === 0) {
        startTimer();
    }
    const questionElement = document.getElementById('question');
    const optionsElement = document.getElementById('options');
    const resultElement = document.getElementById('result');
    resultElement.innerText = '';

    const currentQuestion = quizData[currentQuestionIndex];
    questionElement.innerText = currentQuestion.question;
    optionsElement.innerHTML = '';
    currentQuestion.options.forEach(option => {
        const li = document.createElement('li');
        li.classList.add('list-group-item');
        li.innerHTML = `<label><input type="radio" name="option" value="${option}"> ${option}</label>`;
        optionsElement.appendChild(li);
    });

    const attemptElement = document.getElementById('attempt');
    attemptElement.innerText = `Attempted Questions: ${currentQuestionIndex + 1} / ${quizData.length}`;
}

function checkAnswer() {
    const selectedOption = document.querySelector('input[name="option"]:checked');
    if (!selectedOption) {
        alert("Please select an answer!");
        return;
    }
    const answer = selectedOption.value;
    const resultElement = document.getElementById('result');
    const correctAnswer = quizData[currentQuestionIndex].correct;

    const optionItems = document.querySelectorAll('.list-group-item');
    optionItems.forEach(item => {
        const input = item.querySelector('input[name="option"]');
        if (input.value === correctAnswer) {
            item.classList.add('correct');
        } else if (input.checked) {
            item.classList.add('incorrect');
        }
    });

    if (answer === correctAnswer) {
        resultElement.innerHTML = `<p class="correct">Correct!</p>`;
        score++; // Increment the score for the correct answer
    } else {
        resultElement.innerHTML = `<p class="incorrect">Wrong. The correct answer is ${correctAnswer}</p>`;
    }

    currentQuestionIndex++;
    if (currentQuestionIndex < quizData.length) {
        setTimeout(loadQuestion, 2200);
    } else {
        setTimeout(showFinalScore, 2200);
    }
}

function showFinalScore() {
    clearInterval(timerInterval);
    const quizContainer = document.querySelector('.quiz-container');
    quizContainer.innerHTML = `<h2>Your Score: ${score} out of ${quizData.length}</h2>`;
}

